package com.coding.exercise.data;

/**
 * POJO for JSON mapping of input songs
 */
public class Song
{
    private String id;
    private String artist;
    private String title;

    public Song()
    {
    }

    public String getId()
    {
        return id;
    }

    public String getArtist()
    {
        return artist;
    }

    public String getTitle()
    {
        return title;
    }
}
